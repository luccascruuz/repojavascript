function lerextenso(x){
    switch(x){
        case 0: {
            console.log('zero')
            break
        }
        case 1: {
            console.log('um')
            break
        }
        case 2: {
            console.log('dois')
            break
        }
        default:{
            console.log("número inválido")
        }
    }
}